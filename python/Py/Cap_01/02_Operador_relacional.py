# Operadores relacionales
"""
    >   Mayor que
    <   Menor que 
    >=  Mayor o igual que
    <=  Menor o igual que
    !=  Diferente de
    ==  Igual que
"""
print("_________________________________________\n")
print("10<20")
res1=10<20
print(res1,"\n")

print("10>20")
res2=10>20
print(res2,"\n")

print("1>=0")
res3=1>=0
print(res3,"\n")

print("8<=30")
res4=8<=30
print(res4,"\n")

print("1000!=1000")
res5=1000!=1000
print(res5,"\n")

print("2==20")
res6=2==20
print(res6,"\n")